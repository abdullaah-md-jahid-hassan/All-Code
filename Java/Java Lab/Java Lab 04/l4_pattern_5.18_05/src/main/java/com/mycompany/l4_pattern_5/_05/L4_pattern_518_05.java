package com.mycompany.l4_pattern_5._05;

public class L4_pattern_518_05 {

    public static void main(String[] args) {
        for(int i=1;i<7;i++){
            int n=1;
           for(int j=1;j<=i;j++){
               System.out.print(n+"\t");
               n++;
           } 
            System.out.print("\n");
        }
    }
}
